

//this is like a guidance so that everyone uses the same methods
public class Person
{
	
	private String name;
	private int age;
	
	public Person(String name, int age)
	{
		this.setName(name);
		this.age = age;
			
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

	
}
